package com.pipudding.peccancy.type;

public class EventListType {
	String icon;
	
	String eventId;
	
	String eventType;
	
	String commitor;
	
	String commitorName;
	
	String text;

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getEventId() {
		return eventId;
	}

	public void setEventId(String eventId) {
		this.eventId = eventId;
	}

	public String getEventType() {
		return eventType;
	}

	public void setEventType(String eventType) {
		this.eventType = eventType;
	}


	public String getCommitor() {
		return commitor;
	}

	public void setCommitor(String commitor) {
		this.commitor = commitor;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getCommitorName() {
		return commitorName;
	}

	public void setCommitorName(String commitorName) {
		this.commitorName = commitorName;
	}
	
	

}
